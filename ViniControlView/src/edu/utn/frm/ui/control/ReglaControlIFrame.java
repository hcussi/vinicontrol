    private void verReglaRemontajeDia() {
        int index = tablaRemontajeDia.getSelectedRow();
        if (index >= 0) {
            RemontajeDia reglaRemontajeDia = modelRemontajeDia.getRow(index);
            if (reglaRemontajeDia != null) {
                bindingRemontajeDia(reglaRemontajeDia);
                showDatosDialogRemontajeDia(true);
            }
        } else {
            JOptionPane.showInternalMessageDialog(this, "Debe seleccionar un Remontaje Dia para eliminar");
        }
    }

    private void eliminar() {
        ReglaControl reglaControl = null;

        try {
            if (buscarTabla.getSelectedRow() != -1) {
                reglaControl = model.getRow(buscarTabla.getSelectedRow());
                if (reglaControl != null) {

                    int respuesta = JOptionPane.showConfirmDialog(datosDialog, "Está seguro que desea eliminar la Regla de Control seleccionado?",
                            "Confirmar Eliminación", JOptionPane.OK_CANCEL_OPTION);

                    if (respuesta == JOptionPane.OK_OPTION) {
                        gestorAbm.setEntity(reglaControl);
                        gestorAbm.eliminar();
                        reset();
                        showDatosDialog(false);
                    }
                }
            }
            if (reglaControl == null) {
                JOptionPane.showInternalMessageDialog(this, "Debe seleccionar una Regla de Control para eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

