    private final int cliente = 0;
    private final int servidor = 1;

    public void bindinDataEntity(int fuente) {
        if (fuente == servidor) {
            codigoText.setText(getEntity().getCodigo());
            descripcionText.setText(getEntity().getDescripcion());
            modelTemperatura.setSelectedEntity(getEntity().getReglaControlTemperatura());
            if (getReglaTemperatura() == null) {
                verTemperaturaButton.setEnabled(false);
            } else {
                verTemperaturaButton.setEnabled(true);
            }
            modelNivel.setSelectedEntity(getEntity().getReglaControlNivel());
            if (getReglaNivel() == null) {
                verNivelButton.setEnabled(false);
            } else {
                verNivelButton.setEnabled(true);
            }
            modelRemontaje.setSelectedEntity(getEntity().getReglaControlRemontaje());
            if (getReglaRemontaje() == null) {
                verRemontajeButton.setEnabled(false);
            } else {
                verRemontajeButton.setEnabled(true);
            }
        } else if (fuente == cliente) {
            getEntity().setCodigo(codigoText.getText());
            getEntity().setDescripcion(descripcionText.getText());
            getEntity().setReglaControlTemperatura(modelTemperatura.getSelectedEntity());
            getEntity().setReglaControlNivel(modelNivel.getSelectedEntity());
            getEntity().setReglaControlRemontaje(modelRemontaje.getSelectedEntity());
        }

    }

