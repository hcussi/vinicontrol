    private void nuevo() {
        gestorAbm.nuevo();
        habilitarPanelDatos(true);
        eliminarButton.setEnabled(false);
        nuevoButton.setEnabled(false);
        limpiarDatosEntityButton.setEnabled(true);
        bindinDataEntity(servidor);
    }

    private void editar() {
        Variedad variedad = null;

        if (buscarTabla.getSelectedRow() != -1) {
            variedad = model.getRow(buscarTabla.getSelectedRow());
            if (variedad != null) {
                gestorAbm.setEntity(variedad);
                bindinDataEntity(servidor);
                habilitarPanelDatos(true);
            }
        }
        if (variedad == null) {
            JOptionPane.showInternalMessageDialog(this, "Debe seleccionar una Variedad para editar");
        }
    }

    private void copiar() {
        Variedad variedad = null;

        if (buscarTabla.getSelectedRow() != -1) {
            variedad = model.getRow(buscarTabla.getSelectedRow());
            if (variedad != null) {
                gestorAbm.setEntity(variedad);
                try {
                    gestorAbm.copiar();
                    reset();
                    JOptionPane.showMessageDialog(this, "Se ha copiado exitosamente", "Información", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
        if (variedad == null) {
            JOptionPane.showInternalMessageDialog(this, "Debe seleccionar una Variedad para copiar");
        }
    }

    private void guardar() {

        bindinDataEntity(cliente);
        try {
            gestorAbm.guardar();
            gestorAbm.valid();
            reset();
        } catch (ValidateException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private void eliminar() {
        Variedad variedad = null;

        try {
            if (buscarTabla.getSelectedRow() != -1) {
                variedad = model.getRow(buscarTabla.getSelectedRow());
                if (variedad != null) {

                    int respuesta = JOptionPane.showConfirmDialog(this, "Está seguro que desea eliminar la Variedad seleccionado?",
                            "Confirmar Eliminación", JOptionPane.OK_CANCEL_OPTION);

                    if (respuesta == JOptionPane.OK_OPTION) {
                        gestorAbm.setEntity(variedad);
                        gestorAbm.eliminar();
                        reset();
                    }

                }
            }
            if (variedad == null) {
                JOptionPane.showInternalMessageDialog(this, "Debe seleccionar una Variedad para eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private void reset() {
        codigoText.setText("");
        descripcionText.setText("");
        tinteText.setText("");
        setEntity(null);
        habilitarPanelDatos(false);
        model.setRows(gestorAbm.buscar());
    }

